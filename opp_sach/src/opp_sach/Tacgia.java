package opp_sach;

public class Tacgia {
	private String ten;
	private Ngay ngay;
	
	public Tacgia(String ten, Ngay ngay) {
		super();
		this.ten = ten;
		this.ngay = ngay;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public Ngay getNgay() {
		return ngay;
	}
	public void setNgay(Ngay ngay) {
		this.ngay = ngay;
	}
	
	
}
